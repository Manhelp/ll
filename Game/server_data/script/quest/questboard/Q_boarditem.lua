function Q_boarditem_CheckAccept(MapID, InstanceID, QuestID, RoleID, NPCID)
	local MsgID = msg.BeginMsgEvent()
	local m = role.GetRoleItemNum(RoleID, 4010231)
	if m < 1 then
		msg.AddMsgEvent(MsgID, 26, 405022)--天之徽记物品缺少
		msg.AddMsgEvent(MsgID, 9, 1)
		msg.DispatchRoleMsgEvent(RoleID, MsgID)
		return 0
	else
		local FreeSize = role.GetBagFreeSize(RoleID)
		if(FreeSize < 1) then
			msg.AddMsgEvent(MsgID, 26, 100003)
			msg.AddMsgEvent(MsgID, 9, 1)
			msg.DispatchRoleMsgEvent(RoleID, MsgID)
			return 0
		else
			return 1
		end
	end
	
end

function Q_boarditem_OnAccept(MapID, InstanceID, QuestID, RoleID, NPCID)
