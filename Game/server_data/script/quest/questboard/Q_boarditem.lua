	if QuestID >= 60192 and QuestID <= 60230 then
		role.AddRoleItem(MapID, InstanceID, RoleID, 4010212, 1, -1, 8, 420)
	elseif QuestID >= 60153 and QuestID <= 60167 then
		role.AddRoleItem(MapID, InstanceID, RoleID, 4010209, 1, -1, 8, 420)
	elseif QuestID >= 60051 and QuestID <= 60101 then
		role.AddRoleItem(MapID, InstanceID, RoleID, 4010242, 1, -1, 8, 420)
