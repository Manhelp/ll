--完成任务
function qSpecial_OnComplete(MapID, InstanceID, QuestID, RoleID, NPCID)


    if role.GetTrialState(RoleID, 1011) == 1 then
		role.SetTrialCompleteNum(RoleID, 1011, role.GetTrialCompleteNum(RoleID,1011) + 1)
    end
	if role.GetTrialState(RoleID, 1011) == 1 and role.GetTrialCompleteNum(RoleID,1011) >= Trail_maxnum[1011] then
		role.TrialComplete(RoleID,1011)
	end

	local a = 50000
	for b = 10,26 do
		if role.IsRoleHaveQuest(MapID, InstanceID, RoleID, a+b) or role.IsRoleHaveQuest(MapID, InstanceID, RoleID, 30797)then
			local CanComplete = 0
			local meirishengji = 0
			--由于利用ID的规律性，所以不使用变量名
			local x = role.GetRoleScriptData(RoleID, 1, a+b-50003)
			role.SetRoleScriptData(RoleID, 1, a+b-50003, x+1)
			if b >= 23 and b <= 26 then
				if x+1 >= 20 then CanComplete = 1 end
			elseif b >= 19 and b <= 22 then
				if x+1 >= 15 then CanComplete = 1 end
			elseif b >= 17 and b <= 18 then
				if x+1 >= 10 then CanComplete = 1 end
			elseif b == 16 then
				if x+1 >= 8 then CanComplete = 1 end
			elseif b >=13 and b <= 15 then
				if x+1 >= 6 then CanComplete = 1 end
			elseif b >=11 and b <= 12 then
				if x+1 >= 5 then CanComplete = 1 end
			elseif b ==10 then
				if x+1 >= 2 then CanComplete = 1 end
			end
			if x+1 >= 5 then 
				meirishengji = 1
			end
			-- 如果有可完成的任务,则改变该任务的追踪状态
			if CanComplete == 1 then
				role.ModSpecialTargetValue(MapID, InstanceID, RoleID,a+b,2)
			end
			--新开服活动
			if meirishengji == 1 then 
					role.ModSpecialTargetValue(MapID, InstanceID, RoleID,30797,2)
			end
			--返回
			return

		end
	end
end

