function zhanchangguai_OnDie(MapID, InstanceID, TargetID, TargetTypeID, RoleID)
	local g = 0 
	if TargetTypeID >= 4020000 and TargetTypeID <= 4020065 then
		g = 1
	elseif TargetTypeID >= 4020066 and TargetTypeID <= 4020088 then
		g = 2
	elseif TargetTypeID >= 4020089 and TargetTypeID <= 4020107 then
		g = 4
	elseif TargetTypeID >= 4020108 and TargetTypeID <= 4020136 then
		g = 5
	end
	role.SetWarGold(MapID, InstanceID,RoleID,g)
end
